package org.wyona.security.core.api;

/**
 *
 */
public class AccessManagementException extends Exception {

    /**
     *
     */
    public AccessManagementException() {
        super();
    }

    /**
     *
     */
    public AccessManagementException(Throwable t) {
        super(t);
    }

    /**
     *
     */
    public AccessManagementException(String s) {
        super(s);
    }

    /**
     *
     */
    public AccessManagementException(String s, Throwable t) {
        super(s, t);
    }

}
